def get_distance(departure: str, destination: str) -> Optional[int]:
    """Get flight distance in km for a city pair.

    Returns None if distance is unknown and cannot be estimated.
    """
    # Direct lookup
    key = (departure, destination)
    if key in HARDCODED_DISTANCES:
        return HARDCODED_DISTANCES[key]

    # Reverse direction
    reverse_key = (destination, departure)
    if reverse_key in HARDCODED_DISTANCES:
        return HARDCODED_DISTANCES[reverse_key]

    # No hardcoded value — return None (caller should use fallback)
    return None


def get_distance_with_fallback(departure: str, destination: str, duration_minutes: int = 0) -> float:
    """Get distance with fallback to estimation from flight duration.

    Args:
        departure: Departure city name
        destination: Destination city name
        duration_minutes: Flight duration in minutes (used for estimation if hardcoded not available)
    """
    hardcoded = get_distance(departure, destination)
    if hardcoded is not None:
        return float(hardcoded)

    # Estimate from duration: assume 750 km/h average speed (with taxi time subtracted)
    if duration_minutes > 30:
        # Subtract ~30 min for taxi/climb/descent, then 750 km/h
        cruise_minutes = max(30, duration_minutes - 30)
        estimated = cruise_minutes * 750 / 60
        return float(estimated)

